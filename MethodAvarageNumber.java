public class MethodAvarageNumber {
    public static void main(String[] args) {
        int number1=20;
        int number2=40;
        int number3=70;
        System.out.println("The avarage number :"+avarage(number1,number2,number3));

    }

    public static double avarage(double number1,double number2,double number3) {
        return(number1+number2+number3)/3;
    }
}
